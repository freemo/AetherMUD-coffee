package com.syncleus.aethermud.game.Abilities.Songs;

import com.syncleus.aethermud.game.Abilities.interfaces.Ability;
import com.syncleus.aethermud.game.Common.interfaces.PhyStats;
import com.syncleus.aethermud.game.core.CMLib;
import com.syncleus.aethermud.game.core.CMath;
import com.syncleus.aethermud.game.core.interfaces.Physical;


public class Dance_Musette extends Dance {
    private final static String localizedName = CMLib.lang().L("Musette");

    @Override
    public String ID() {
        return "Dance_Musette";
    }

    @Override
    public String name() {
        return localizedName;
    }

    @Override
    public int abstractQuality() {
        return Ability.QUALITY_MALICIOUS;
    }

    @Override
    protected String danceOf() {
        return name() + " Dance";
    }

    @Override
    public void affectPhyStats(Physical affected, PhyStats affectableStats) {
        super.affectPhyStats(affected, affectableStats);
        if (invoker == null)
            return;
        if (invoker == affected)
            return;

        affectableStats.setSpeed(CMath.div(affectableStats.speed(), 2.0 + CMath.mul(super.getXLEVELLevel(invoker()), 0.30)));
    }
}
